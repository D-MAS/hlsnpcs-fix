if IsValid(gib) then
gib:SetPos(dmginfo:GetDamagePosition())
gib:SetAngles(npc:GetAngles())
gib:SetOwner(npc)
gib:Spawn()
if table.HasValue(HLSNPC_Ignite_Head_Damage, dmginfo:GetDamageType()) then
