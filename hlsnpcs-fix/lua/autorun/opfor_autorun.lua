if dmginfo:GetDamage() <= 0 or (attacker:IsNPC() and (attacker:Disposition(npc) == D_LI or attacker:Disposition(npc) == D_NU)) then return end
local damage = dmginfo:GetDamage()
local headhp = npc:Health()*.45
dmginfo:ScaleDamage(5)
npc:SetHealth(1)
if IsValid(attacker:GetActiveWeapon()) then
npc:TakeDamage(1, attacker, attacker:GetActiveWeapon())
else
npc:TakeDamage(1, attacker, attacker)
end
local headshotblood = EffectData()
headshotblood:SetEntity(npc)
headshotblood:SetOrigin(dmginfo:GetDamagePosition())
util.Effect("hlsnpc_headshot", headshotblood)
for i=0,50 do
local Name=string.lower(npc:GetBoneName(i))
if(string.find(Name,"head"))and(damage >= headhp)then
npc:ManipulateBoneScale(i,Vector(0,0,0))
if(damage >= headhp)and(!npc:GetNWBool("HLSNPC_NoHead"))then
npc:SetNWBool("HLSNPC_NoHead", true)
npc:EmitSound("HLSNPC_Gib.HeadShot")
