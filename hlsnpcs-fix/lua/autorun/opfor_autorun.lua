gibcloud:SetMagnitude(2)
if npc:GetBloodColor()==0 then
util.Effect("hlsnpc_gib", gibcloud)
else
util.Effect("hlsnpc_gibyell", gibcloud)
end
elseif(damage >= gibhp*1.25)then
dmginfo:SetDamageType(DMG_ALWAYSGIB)
